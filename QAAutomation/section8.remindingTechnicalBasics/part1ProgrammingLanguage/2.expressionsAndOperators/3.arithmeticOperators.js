"use strict";

//Arithmetic Operators moreover than (+,-,/,*) there are:

//Increment ++
let i = 0
console.log(++i);
console.log(i++);
console.log(i);

//Decrement --
let d = 0;
console.log(--d);
console.log(d--);
console.log(d);

//Unary Negation
let u = -1;
console.log(u);

//Unary Plus
let up = +true;
console.log(up);

//Exponentiation
console.log(3 ** 3);
