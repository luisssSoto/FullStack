"use strict";

//Equal == change the type of the operand before compare
console.log(3 == '3');

//Not Equal !== 
console.log(3 != '3');

//Strict Equal === doesn't cast the operands before, neither after
console.log(3 === '3');

//Strict Not Equal !==
console.log(3 !== '3');

//Greater Than >
console.log(3 > 2);

//Greater than or Equal
console.log(3 >= 3);

//Less than
console.log(3 < 4);

//Less than or Equal
console.log(4 <= 4);